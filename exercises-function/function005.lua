-- faça uma calculadora


function somarDoisNumeros(a, b)
  print("soma: " .. a + b)
end

somarDoisNumeros(31,2)

function sub(c, d)
  print("subtração: ".. c - d)
end

sub(10,6)

function multiplicar(e,f)
  print("multiplicação: ".. e * f)
end

multiplicar(8,2)

function dividir(g,h)
  print("divisão: ".. g / h)
end

dividir(8,2)